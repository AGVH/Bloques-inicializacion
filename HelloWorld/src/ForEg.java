public class ForEg {
    public static void main(String[] args) {
        String [] string = {"a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q",
        "r","s","t","u","v","w","x","y","z"};
        for (int i = 0; i < string.length; i++) {
            System.out.print(string[i].toUpperCase()+" ");
        }
        System.out.println();
    }
}
